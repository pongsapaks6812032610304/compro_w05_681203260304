#include <stdio.h>

int main() {
    char name[3]; // ประกาศตัวแปรสำหรับเก็บชื่อเล่น
    int age[3];  // ประกาศตัวแปรสำหรับเก็บอายุ
    float height[3]; // ประกาศตัวแปรสำหรับเก็บส่วนสูง
    float weight[3]; // ประกาศตัวแปรสำหรับเก็บน้ำหนัก
    char grade[3]; // ประกาศตัวแปรสำหรับเก็บเกรด

    // รับข้อมูลจากผู้ใช้จำนวน 3 คน
    for(int i = 0; i < 3; i++) { // วนลูปรับข้อมูล 3 คน
        printf("Enter data for person %d (Name*Age*Height*Weight*Grade): ", i + 1); // ข้อความให้ผู้ใช้ป้อนข้อมูล
        scanf(" %c*%d*%f*%f*%c", &name[i], &age[i], &height[i], &weight[i], &grade[i]);
        // รับข้อมูลจากผู้ใช้ด้วย scanf รับชื่อเล่น, อายุ, ส่วนสูง, น้ำหนัก, เกรด
        //โดยใช้ %c สำหรับตัวอักษร, %d สำหรับจำนวนเต็ม, %f สำหรับจำนวนทศนิยมและใช้ & เพื่อส่งที่อยู่ของตัวแปร
    }

    // แสดงผลลัพธ์เป็นตาราง
    printf("\n+--------+-----------+------------+------------+-------------+\n"); // เส้นขอบบนของตาราง
    printf("| Name   | Height(cm)| Weight(kg) | Grade Code |\n");  // หัวตาราง
    printf("+--------+-----------+------------+-------------+\n"); // เส้นขอบกลางของตาราง

    for(int i = 0; i < 3; i++) {  // วนลูปแสดงข้อมูลของแต่ละคน
        printf("|   %-3c  |   %6.1f  |   %6.1f   |     %-3c    |\n", name[i], height[i], weight[i], grade[i]);
        // แสดงข้อมูลในรูปแบบตาราง
    }

    printf("+--------+-----------+------------+-------------+\n"); // เส้นขอบล่างของตาราง

    return 0; // คืนค่า 0 เพื่อบอกว่าการทำงานสำเร็จ
}
